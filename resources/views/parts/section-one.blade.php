                <h2>What is "What's the WP Theme"?</h2>
                <p>
                    A tool to discover <a href="https://wordpress.com" target="_blank">Wordpress</a> sites theme
                    built on top of <a href="https://laravel.com" target="_blank">Laravel</a>.
                <p>
                <h3>How do you do that?</h3>
                <p>
                    Each site has a `style.css` file with the theme information, even child themes.
                    We just do some magic to get this file, even in sites using minification
                    (like done by caching plugins such as <a href="https://wordpress.org/plugins/w3-total-cache/" target="_blank">W3 Total Cache</a>,
                    <a href="https://wordpress.org/plugins/wp-super-cache/" target="_blank">WP Super Cache</a> and similars).
                </p>
                <h3>Why do you do that?</h3>
                <p>
                    This is a small <a href="" target="_blank">open source</a> project, did as a research.
                    The solution have been done from scratch, although there are very sophisticated tools such as
                    <a href="https://wpscan.org/" target="_blank">WPScan</a> that have many ways to achieve the same 
                    goals (even deeper, actually).
                </p>
